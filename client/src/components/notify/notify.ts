import { toast } from "react-toastify";

export const notifySucces = () =>
    toast.success("Todo was successfully added:)", {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });

